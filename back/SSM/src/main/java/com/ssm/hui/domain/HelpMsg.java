public class HelpMsg {
	private int helpId;
	private String helpTitle; 
	private String helpContent;
	private String otherRemark;
	/**
	 * @return the helpId
	 */
	public int getHelpId() {
		return helpId;
	}
	/**
	 * @param helpId the helpId to set
	 */
	public void setHelpId(int helpId) {
		this.helpId = helpId;
	}
	/**
	 * @return the helpTitle
	 */
	public String getHelpTitle() {
		return helpTitle;
	}
	/**
	 * @param helpTitle the helpTitle to set
	 */
	public void setHelpTitle(String helpTitle) {
		this.helpTitle = helpTitle;
	}
	/**
	 * @return the helpContent
	 */
	public String getHelpContent() {
		return helpContent;
	}
	/**
	 * @param helpContent the helpContent to set
	 */
	public void setHelpContent(String helpContent) {
		this.helpContent = helpContent;
	}
	/**
	 * @return the otherRemark
	 */
	public String getOtherRemark() {
		return otherRemark;
	}
	/**
	 * @param otherRemark the otherRemark to set
	 */
	public void setOtherRemark(String otherRemark) {
		this.otherRemark = otherRemark;
	}
	
	
}
